package com.example.ideskdemo.object;

public class Attends {

    String attendsID;
    String attendsDate;
    String attendsStatus;
    String attendsMarkerID;
    String attendsStudent;


    public Attends(){

    }

    public String getAttendsID() {
        return attendsID;
    }

    public void setAttendsID(String attendsID) {
        this.attendsID = attendsID;
    }

    public String getAttendsDate() {
        return attendsDate;
    }

    public void setAttendsDate(String attendsDate) {
        this.attendsDate = attendsDate;
    }

    public String getAttendsStatus() {
        return attendsStatus;
    }

    public void setAttendsStatus(String attendsStatus) {
        this.attendsStatus = attendsStatus;
    }

    public String getAttendsMarkerID() {
        return attendsMarkerID;
    }

    public void setAttendsMarkerID(String attendsMarkerID) {
        this.attendsMarkerID = attendsMarkerID;
    }

    public String getAttendsStudent() {
        return attendsStudent;
    }

    public void setAttendsStudent(String attendsStudent) {
        this.attendsStudent = attendsStudent;
    }
}
